import React, { useRef, useState } from 'react';
import { SearchOutlined } from '@ant-design/icons';
import { Button, Input, Space, Table, Tag } from 'antd';
import Highlighter from 'react-highlight-words';
import AddAttritionSection from './add-attrition-section';
import { useSelector } from 'react-redux';
import ButtonComponents from '../components/button-components';
import store from '@/app/store/store';
import { get_employee_thunk } from '../../../employee_relation/employee_section/redux/employee-section-thunk';
import { useEffect } from 'react';
import AttritionMenuSection from './attrition-menu-section';
import moment from 'moment';

export default function AttritionTableSection() {
    const [searchText, setSearchText] = useState('');
    const [searchedColumn, setSearchedColumn] = useState('');
    const searchInput = useRef(null);
    const { employee_attritions } = useSelector((state) => state.employee_attritions)
    console.log('attrition', employee_attritions)

    useEffect(() => {
        store.dispatch(get_employee_thunk())
    }, []);

    const handleSearch = (selectedKeys, confirm, dataIndex) => {
        confirm();
        setSearchText(selectedKeys[0]);
        setSearchedColumn(dataIndex);
    };
    const handleReset = (clearFilters) => {
        clearFilters();
        setSearchText('');
    };

    const getColumnSearchProps = (dataIndex) => ({
        filterDropdown: ({ setSelectedKeys, selectedKeys, confirm, clearFilters, close }) => (
            <div
                style={{
                    padding: 8,
                }}
                onKeyDown={(e) => e.stopPropagation()}
            >
                <Input
                    ref={searchInput}
                    placeholder={`Search ${dataIndex}`}
                    value={selectedKeys[0]}
                    onChange={(e) => setSelectedKeys(e.target.value ? [e.target.value] : [])}
                    onPressEnter={() => handleSearch(selectedKeys, confirm, dataIndex)}
                    style={{
                        marginBottom: 8,
                        display: 'block',
                    }}
                />
                <Space>
                    <Button
                        type="primary"
                        onClick={() => handleSearch(selectedKeys, confirm, dataIndex)}
                        icon={<SearchOutlined />}
                        size="small"
                        style={{
                            width: 90,
                        }}
                    >
                        Search
                    </Button>
                    <Button
                        onClick={() => clearFilters && handleReset(clearFilters)}
                        size="small"
                        style={{
                            width: 90,
                        }}
                    >
                        Reset
                    </Button>
                    <Button
                        type="link"
                        size="small"
                        onClick={() => {
                            confirm({
                                closeDropdown: false,
                            });
                            setSearchText(selectedKeys[0]);
                            setSearchedColumn(dataIndex);
                        }}
                    >
                        Filter
                    </Button>
                    <Button
                        type="link"
                        size="small"
                        onClick={() => {
                            close();
                        }}
                    >
                        close
                    </Button>
                </Space>
            </div>
        ),
        filterIcon: (filtered) => (
            <SearchOutlined
                style={{
                    color: filtered ? '#1677ff' : undefined,
                }}
            />
        ),
        onFilter: (value, record) =>
            record[dataIndex].toString().toLowerCase().includes(value.toLowerCase()),
        onFilterDropdownOpenChange: (visible) => {
            if (visible) {
                setTimeout(() => searchInput.current?.select(), 100);
            }
        },
        render: (text) =>
            searchedColumn === dataIndex ? (
                <Highlighter
                    highlightStyle={{
                        backgroundColor: '#ffc069',
                        padding: 0,
                    }}
                    searchWords={[searchText]}
                    autoEscape
                    textToHighlight={text ? text.toString() : ''}
                />
            ) : (
                text
            ),
    });

    const columns = [
        {
            title: 'Employee No.',
            dataIndex: 'emp_id',
            key: 'emp_id',
            // ...getColumnSearchProps('emp_id'),
        },
        {
            title: 'Employee Name',
            dataIndex: 'fullname',
            key: 'fullname',
            // ...getColumnSearchProps('fullname'),
            render: (_, record, i) => {
                return (
                    <div key={i}>
                        {record?.applicant?.fname} {record?.applicant?.mname} {record?.applicant?.lname}
                    </div>
                )
            }
        },
        {
            title: 'Position',
            dataIndex: 'position',
            key: 'position',
            // ...getColumnSearchProps('position'),
        },
        {
            title: 'Department',
            dataIndex: 'dept',
            key: 'dept',
            // ...getColumnSearchProps('dept'),
        },
        {
            title: 'Hired',
            dataIndex: 'hired',
            key: 'hired',
            render: (_, record) => {
                return (
                    <div className="gap-1.5 flex">
                        {record.hired ? moment(record.hired).format('LL') : ''}
                    </div>
                );
            },
            // ...getColumnSearchProps('hired'),
        },
        {
            title: 'Separation',
            dataIndex: 'separation',
            key: 'separation',
            render: (_, record) => {
                return (
                    <div className="gap-1.5 flex">
                        {record.separation ? moment(record.separation).format('LL') : ''}
                    </div>
                );
            },
            // ...getColumnSearchProps('separation'),
        },
        {
            title: 'Employment Status',
            dataIndex: 'status',
            key: 'status',
            render: (_, record) => {
                let color = '';
                switch (record.status) {
                    case 'Probationary':
                        color = '#52D017';
                        break;
                    case 'Regular':
                        color = '#43BFC7';
                        break;
                }
                return (
                    <Tag color={color} key={record.key}>
                        {record.status}
                    </Tag>
                );
            },
        },
        {
            title: 'Reason for Separation',
            dataIndex: 'reas',
            key: 'reas',
            render: (_, record) => {
                return (
                    <Tag color="#FF0000" key={record.key}>
                        {record.reas}
                    </Tag>
                );
            },
        },
        {
            title: 'Exit Clearance Status',
            dataIndex: 'estatus',
            key: 'estatus',
            render: (_, record) => {
                let color = '';
                let textColor = '#000'; // default text color

                switch (record.estatus) {
                    case 'Pending':
                        color = '#E1AD01';
                        textColor = '#fff';
                        break;
                    case 'Cleared':
                        color = '#52D017';
                        textColor = '#fff';
                        break;
                    case 'Pending Clearance':
                        color = '#FFFF00';   // background
                        textColor = '#000';  // black text for yellow background
                        break;
                    default:
                        color = '#d9d9d9';
                        textColor = '#000';
                }

                return (
                    <Tag
                        key={record.key}
                        style={{ backgroundColor: color, color: textColor, border: 'none' }}
                    >
                        {record.estatus}
                    </Tag>
                );
            },
        },
        {
            title: 'Action',
            dataIndex: 'action',
            render: (_, record) => {
                return (
                    <h1>
                        {/* <ButtonComponents/> */}
                        <AttritionMenuSection data={record} />
                    </h1>
                )
            }
        },
    ];

    return (
        <div>
            <div>
                <div className="flex items-center gap-x-3 mb-4">
                    <h2 className="text-lg font-medium text-gray-800">
                        <b>Employee Attrition Records</b>
                    </h2>
                </div>
            </div>
            <AddAttritionSection />
            <Table columns={columns} dataSource={employee_attritions} />;
        </div>
    );
};
